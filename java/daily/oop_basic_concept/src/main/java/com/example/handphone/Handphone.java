package com.example.handphone;

public class Handphone {
    
    void turnOn() {
        System.out.println("Phone comes to life...");
    }

    void sendCall() {
        System.out.println("Ring, ring, ring... call initiated");
    }

    void sendMessage() {
        System.out.println("Ding, ding... message sent");
    }
    
    void turnOff() {
        System.out.println("Phone powers down...");
    }

}
