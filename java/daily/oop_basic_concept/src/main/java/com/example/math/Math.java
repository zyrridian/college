package com.example.math;

public class Math {
    
    int addition(int a, int b) {
        return a + b;
    }

    int substraction(int a, int b) {
        return a - b;
    }

    int multiplication(int a, int b) {
        return a * b;
    }

    double division(double a, double b) {
        return a / b;
    }

}
